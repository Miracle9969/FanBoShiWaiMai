package cn.waimai.pojo;

public class SiteProvice {
	private Integer site_provice_id;
	private String site_provice_name;
	private Integer site_city_id;
	public String getSite_provice_name() {
		return site_provice_name;
	}
	public void setSite_provice_name(String site_provice_name) {
		this.site_provice_name = site_provice_name;
	}
	public Integer getSite_provice_id() {
		return site_provice_id;
	}
	public void setSite_provice_id(Integer site_provice_id) {
		this.site_provice_id = site_provice_id;
	}
	public Integer getSite_city_id() {
		return site_city_id;
	}
	public void setSite_city_id(Integer site_city_id) {
		this.site_city_id = site_city_id;
	}
}
