package cn.waimai.pojo;

public class District {
	private Integer district_id;
	private String district_name;
	private Integer district_city_id;
	public Integer getDistrict_id() {
		return district_id;
	}
	public void setDistrict_id(Integer district_id) {
		this.district_id = district_id;
	}
	public String getDistrict_name() {
		return district_name;
	}
	public void setDistrict_name(String district_name) {
		this.district_name = district_name;
	}
	public Integer getDistrict_city_id() {
		return district_city_id;
	}
	public void setDistrict_city_id(Integer district_city_id) {
		this.district_city_id = district_city_id;
	}
}
