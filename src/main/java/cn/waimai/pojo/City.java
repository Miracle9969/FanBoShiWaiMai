package cn.waimai.pojo;

public class City {
	private Integer city_id;
	private String city_name;
	private Integer city_district_id;
	public Integer getCity_id() {
		return city_id;
	}
	public void setCity_id(Integer city_id) {
		this.city_id = city_id;
	}
	public String getCity_name() {
		return city_name;
	}
	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}
	public Integer getCity_district_id() {
		return city_district_id;
	}
	public void setCity_district_id(Integer city_district_id) {
		this.city_district_id = city_district_id;
	}
}
