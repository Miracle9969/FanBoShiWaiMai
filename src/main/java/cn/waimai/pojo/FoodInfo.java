package cn.waimai.pojo;

public class FoodInfo {
	private Integer food_id;
	private Integer food_shop_id;
	private String food_name;
	private Float food_price;
	private Integer food_sales;
	public Integer getFood_id() {
		return food_id;
	}
	public void setFood_id(Integer food_id) {
		this.food_id = food_id;
	}
	public Integer getFood_shop_id() {
		return food_shop_id;
	}
	public void setFood_shop_id(Integer food_shop_id) {
		this.food_shop_id = food_shop_id;
	}
	public String getFood_name() {
		return food_name;
	}
	public void setFood_name(String food_name) {
		this.food_name = food_name;
	}
	public Float getFood_price() {
		return food_price;
	}
	public void setFood_price(Float food_price) {
		this.food_price = food_price;
	}
	public Integer getFood_sales() {
		return food_sales;
	}
	public void setFood_sales(Integer food_sales) {
		this.food_sales = food_sales;
	}
}
