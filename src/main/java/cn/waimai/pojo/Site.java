package cn.waimai.pojo;

public class Site {
	private Integer site_id;
	private Integer site_user_id;
	private Integer site_shop_id;
	private Integer site_provice_id;
	public Integer getSite_id() {
		return site_id;
	}
	public void setSite_id(Integer site_id) {
		this.site_id = site_id;
	}
	public Integer getSite_user_id() {
		return site_user_id;
	}
	public void setSite_user_id(Integer site_user_id) {
		this.site_user_id = site_user_id;
	}
	public Integer getSite_shop_id() {
		return site_shop_id;
	}
	public void setSite_shop_id(Integer site_shop_id) {
		this.site_shop_id = site_shop_id;
	}
	public Integer getSite_provice_id() {
		return site_provice_id;
	}
	public void setSite_provice_id(Integer site_provice_id) {
		this.site_provice_id = site_provice_id;
	}
}
