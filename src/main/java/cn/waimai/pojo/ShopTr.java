public class ShopTr {
	private Integer tr_id;
	private Integer tr_shop_id;
	private String tr_num;
	private String tr_name;
	private String tr_address;
	private Integer tr_beginTime;
	private Integer tr_endTime;
	public Integer getTr_id() {
		return tr_id;
	}
	public void setTr_id(Integer tr_id) {
		this.tr_id = tr_id;
	}
	public Integer getTr_shop_id() {
		return tr_shop_id;
	}
	public void setTr_shop_id(Integer tr_shop_id) {
		this.tr_shop_id = tr_shop_id;
	}
	public String getTr_num() {
		return tr_num;
	}
	public void setTr_num(String tr_num) {
		this.tr_num = tr_num;
	}
	public String getTr_name() {
		return tr_name;
	}
	public void setTr_name(String tr_name) {
		this.tr_name = tr_name;
	}
	public String getTr_address() {
		return tr_address;
	}
	public void setTr_address(String tr_address) {
		this.tr_address = tr_address;
	}
	public Integer getTr_beginTime() {
		return tr_beginTime;
	}
	public void setTr_beginTime(Integer tr_beginTime) {
		this.tr_beginTime = tr_beginTime;
	}
	public Integer getTr_endTime() {
		return tr_endTime;
	}
	public void setTr_endTime(Integer tr_endTime) {
		this.tr_endTime = tr_endTime;
	}
	

}
