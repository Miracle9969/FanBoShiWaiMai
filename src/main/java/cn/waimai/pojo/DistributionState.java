package cn.waimai.pojo;

public class DistributionState {
	private Integer state_id;
	private Integer state_distribution_id;
	private String state_state;
	public Integer getState_id() {
		return state_id;
	}
	public void setState_id(Integer state_id) {
		this.state_id = state_id;
	}
	public Integer getState_distribution_id() {
		return state_distribution_id;
	}
	public void setState_distribution_id(Integer state_distribution_id) {
		this.state_distribution_id = state_distribution_id;
	}
	public String getState_state() {
		return state_state;
	}
	public void setState_state(String state_state) {
		this.state_state = state_state;
	}
	
}
