package cn.waimai.pojo;

public class ShopInfo {
	private Integer shop_id;
	private String shop_name;
	private Integer shop_address;
	private String shop_mobile;
	private String shop_food;
	private String shop_beginTime;
	private String shop_endTime;
	private Float box_cost;
	private Float send_cost;
	private Float floor_send_cost;
	public Integer getShop_id() {
		return shop_id;
	}
	public void setShop_id(Integer shop_id) {
		this.shop_id = shop_id;
	}
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	public Integer getShop_address() {
		return shop_address;
	}
	public void setShop_address(Integer shop_address) {
		this.shop_address = shop_address;
	}
	public String getShop_mobile() {
		return shop_mobile;
	}
	public void setShop_mobile(String shop_mobile) {
		this.shop_mobile = shop_mobile;
	}
	public String getShop_food() {
		return shop_food;
	}
	public void setShop_food(String shop_food) {
		this.shop_food = shop_food;
	}
	public String getShop_beginTime() {
		return shop_beginTime;
	}
	public void setShop_beginTime(String shop_beginTime) {
		this.shop_beginTime = shop_beginTime;
	}
	public String getShop_endTime() {
		return shop_endTime;
	}
	public void setShop_endTime(String shop_endTime) {
		this.shop_endTime = shop_endTime;
	}
	public Float getBox_cost() {
		return box_cost;
	}
	public void setBox_cost(Float box_cost) {
		this.box_cost = box_cost;
	}
	public Float getSend_cost() {
		return send_cost;
	}
	public void setSend_cost(Float send_cost) {
		this.send_cost = send_cost;
	}
	public Float getFloor_send_cost() {
		return floor_send_cost;
	}
	public void setFloor_send_cost(Float floor_send_cost) {
		this.floor_send_cost = floor_send_cost;
	}
	
	
}
