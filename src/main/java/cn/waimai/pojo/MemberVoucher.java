package cn.waimai.pojo;

public class MemberVoucher {
	private Integer voucher_id;
	private Integer voucher_form_id;
	private Integer voucher_money;
	private Integer voucher_sum;
	private String voucher_beginTime;
	private String voucher_endTime;
	public Integer getVoucher_id() {
		return voucher_id;
	}
	public void setVoucher_id(Integer voucher_id) {
		this.voucher_id = voucher_id;
	}
	public Integer getVoucher_form_id() {
		return voucher_form_id;
	}
	public void setVoucher_form_id(Integer voucher_form_id) {
		this.voucher_form_id = voucher_form_id;
	}
	public Integer getVoucher_money() {
		return voucher_money;
	}
	public void setVoucher_money(Integer voucher_money) {
		this.voucher_money = voucher_money;
	}
	public Integer getVoucher_sum() {
		return voucher_sum;
	}
	public void setVoucher_sum(Integer voucher_sum) {
		this.voucher_sum = voucher_sum;
	}
	public String getVoucher_beginTime() {
		return voucher_beginTime;
	}
	public void setVoucher_beginTime(String voucher_beginTime) {
		this.voucher_beginTime = voucher_beginTime;
	}
	public String getVoucher_endTime() {
		return voucher_endTime;
	}
	public void setVoucher_endTime(String voucher_endTime) {
		this.voucher_endTime = voucher_endTime;
	}
	
}
