package cn.waimai.pojo;

public class RefundState {
	private Integer state_id;
	private Integer state_refund_id;
	private String state_name;
	public Integer getState_id() {
		return state_id;
	}
	public void setState_id(Integer state_id) {
		this.state_id = state_id;
	}
	public Integer getState_refund_id() {
		return state_refund_id;
	}
	public void setState_refund_id(Integer state_refund_id) {
		this.state_refund_id = state_refund_id;
	}
	public String getState_name() {
		return state_name;
	}
	public void setState_name(String state_name) {
		this.state_name = state_name;
	}
}
